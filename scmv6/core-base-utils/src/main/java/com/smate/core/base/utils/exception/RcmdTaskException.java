package com.smate.core.base.utils.exception;

/**
 * 推荐任务相关异常
 * 
 * @author hzr
 *
 */

public class RcmdTaskException extends Exception {

  /**
   * 
   */
  private static final long serialVersionUID = 5260055274627442197L;

  public RcmdTaskException() {
    super();
  }

  public RcmdTaskException(String arg0) {
    super(arg0);
  }

  public RcmdTaskException(Throwable arg1) {
    super(arg1);
  }

  public RcmdTaskException(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }

}
