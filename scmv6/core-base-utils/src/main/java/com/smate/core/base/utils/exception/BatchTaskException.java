package com.smate.core.base.utils.exception;

/**
 * BatchTaskException
 * 
 * @author hzr
 *
 */
public class BatchTaskException extends Exception {


  /**
   * 
   */
  private static final long serialVersionUID = 2670998444617751553L;

  public BatchTaskException() {
    super();
  }

  public BatchTaskException(String arg0) {
    super(arg0);
  }

  public BatchTaskException(Throwable arg1) {
    super(arg1);
  }

  public BatchTaskException(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }

}
