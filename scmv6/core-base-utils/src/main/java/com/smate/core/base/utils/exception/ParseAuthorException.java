package com.smate.core.base.utils.exception;

/**
 * 作者操作异常.
 * 
 * @author zb
 * 
 */
public class ParseAuthorException extends Exception {

  /**
   * 
   */
  private static final long serialVersionUID = 6598113796865394705L;

  public ParseAuthorException() {
    super();
  }

  public ParseAuthorException(String arg0) {
    super(arg0);
  }

  public ParseAuthorException(Throwable arg1) {
    super(arg1);
  }

  public ParseAuthorException(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }
}
