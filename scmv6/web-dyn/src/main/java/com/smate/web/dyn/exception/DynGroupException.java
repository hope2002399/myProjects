package com.smate.web.dyn.exception;


/**
 * 
 * 动态异常
 * 
 * @author tsz
 * @since 6.0.1
 * @version 6.0.1
 */
public class DynGroupException extends DynException {

  /**
   * 
   */
  private static final long serialVersionUID = 7990853206826897517L;


  public DynGroupException() {
    super();
  }

  public DynGroupException(String arg0) {
    super(arg0);
  }

  public DynGroupException(Throwable arg1) {
    super(arg1);
  }

  public DynGroupException(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }


}
