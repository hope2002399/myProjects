package com.smate.web.group.exception;


/**
 * 
 * 群组成果异常
 * 
 * @author tsz
 * @since 6.0.1
 * @version 6.0.1
 */
public class GroupPubException extends GroupException {

  /**
   * 
   */
  private static final long serialVersionUID = 7990853206826897517L;


  public GroupPubException() {
    super();
  }

  public GroupPubException(String arg0) {
    super(arg0);
  }

  public GroupPubException(Throwable arg1) {
    super(arg1);
  }

  public GroupPubException(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }


}
