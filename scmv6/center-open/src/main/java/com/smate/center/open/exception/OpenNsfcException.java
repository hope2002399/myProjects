package com.smate.center.open.exception;

/**
 * 
 * NSFC-WS-异常
 * 
 * @author lxz
 * @since 6.0.1
 * @version 6.0.1
 */
public class OpenNsfcException extends OpenException {

  private static final long serialVersionUID = -3415094570292673097L;

  public OpenNsfcException() {
    super();
  }

  public OpenNsfcException(String arg0) {
    super(arg0);
  }

  public OpenNsfcException(Throwable arg1) {
    super(arg1);
  }

  public OpenNsfcException(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }
}
