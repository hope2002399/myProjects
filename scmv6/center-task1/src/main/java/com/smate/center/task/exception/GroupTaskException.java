package com.smate.center.task.exception;

public class GroupTaskException extends TaskException {

  /**
   * 群组任务相关异常
   * 
   * @author LJ
   */
  private static final long serialVersionUID = 4157517660716306580L;

  public GroupTaskException() {
    super();
  }

  public GroupTaskException(String arg0) {
    super(arg0);
  }

  public GroupTaskException(Throwable arg1) {
    super(arg1);
  }

  public GroupTaskException(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }



}
