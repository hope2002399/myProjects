package com.smate.center.task.exception;

public class InputFullTextTaskException extends TaskException {

  /**
   * 成果全文上传任务异常
   */
  private static final long serialVersionUID = -8769375579260475419L;

  public InputFullTextTaskException() {
    super();
  }

  public InputFullTextTaskException(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }

  public InputFullTextTaskException(String arg0) {
    super(arg0);
  }

  public InputFullTextTaskException(Throwable arg1) {
    super(arg1);
  }



}
