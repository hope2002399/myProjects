package com.smate.center.task.exception;

public class PersonTaskException extends TaskException {

  /**
   * 人员操作任务相关异常
   * 
   * @author LJ
   */
  private static final long serialVersionUID = -3260490647282964138L;

  public PersonTaskException() {
    super();
  }

  public PersonTaskException(String arg0) {
    super(arg0);
  }

  public PersonTaskException(Throwable arg1) {
    super(arg1);
  }

  public PersonTaskException(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }

}
